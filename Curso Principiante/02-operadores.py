# Operadores Aritmeticos

print(10 + 2) # Suma
print(10 - 2) # Resta
print(10 * 2) # Multiplicacion
print(10 / 2) # Division
print(10 % 2) # Módulo
print(10 // 2) # Resultado de la division en número entero
print(10 ** 2) # Exponente
print(10 ** 2 + 5 - 15 * 2 / 3) # Todos los operadores se pueden juntar

print("Hola " + "Python") # <= Hola Python
print("Hola " + str(5)) # <= Hola 5
print("Hola " * 5) # <= Hola Hola Hola Hola Hola

